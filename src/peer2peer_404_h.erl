init(Req, Opts) ->
  HTML = <<"<!DOCTYPE html><html><head><title>404 - Not Found",
    "</title></head><body><h1>404 - Not Found</h1></body>",
    "</html>">>,
  Req2 = cowboy_req:reply(404, #{<<"content-type">> => <<"text/html">>}, HTML, Req),
  {ok, Req2, Opts}.
